function decribeCountry(country, population, capitalCity) {
    return `${country} has ${population} people and its capital city is ${capitalCity}`;
}

decribeCountry("Israel", 9217000, "Jerusalem");
decribeCountry("Vietnam", 98517000, "Hanoi");
decribeCountry("Tonga", 107345, "Nuku'alofa");
// A bonus because it was funny
decribeCountry("Holy See", 804, "Vatican City");